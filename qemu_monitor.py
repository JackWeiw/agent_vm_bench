    def check_stress_process(self, stress_pattern):
        try:
            for proc in psutil.process_iter(['cmdline', 'name']):
                try:
                    cl = ' '.join(proc.info['cmdline'] or [])
                    if stress_pattern in cl or stress_pattern in (proc.info['name'] or ''):
                        return True
                except:
                    continue
            return False
        except:
            return False

