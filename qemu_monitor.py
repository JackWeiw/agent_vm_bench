# ==================== .env Configuration Management ====================

ENV_FILE_PATH = '.env'
ENV_REQUIRED_KEYS = ['DEVKIT_PATH', 'KSYS_PATH', 'KSYS_CONFIG_PATH', 'UB_WATCH_PATH', 'DDR_LATENCY_PATH']


def load_env_config() -> dict:
    """Load configuration from .env file

    Returns:
        dict with keys: devkit_path, ksys_path, ksys_config_path, ub_watch_path, devkit_cpu_range
    """
    config = {
        'devkit_path': '',
        'ksys_path': '',
        'ksys_config_path': '',
        'ub_watch_path': '',
        'devkit_cpu_range': '',
        'ddr_latency_path': '',
    }

    if DOTENV_AVAILABLE and os.path.exists(ENV_FILE_PATH):
        load_dotenv(ENV_FILE_PATH)

    # Read from environment variables (set by dotenv or system)
    config['devkit_path'] = os.environ.get('DEVKIT_PATH', '')
    config['ksys_path'] = os.environ.get('KSYS_PATH', '')
    config['ksys_config_path'] = os.environ.get('KSYS_CONFIG_PATH', '')
    config['ub_watch_path'] = os.environ.get('UB_WATCH_PATH', '')
    config['devkit_cpu_range'] = os.environ.get('DEVKIT_CPU_RANGE', '')
    config['ddr_latency_path'] = os.environ.get('DDR_LATENCY_PATH', '')

    return config


def save_env_config(config: dict):
    """Save configuration back to .env file"""
    env_path = ENV_FILE_PATH

    # Create .env file if not exists
    if not os.path.exists(env_path):
        with open(env_path, 'w') as f:
            f.write("# Log collection tools configuration\n")
            f.write("# Generated by qemu_monitor.py\n\n")

    # Write each key
    if DOTENV_AVAILABLE:
        if config.get('devkit_path'):
            set_key(env_path, 'DEVKIT_PATH', config['devkit_path'])
        if config.get('ksys_path'):
            set_key(env_path, 'KSYS_PATH', config['ksys_path'])
        if config.get('ksys_config_path'):
            set_key(env_path, 'KSYS_CONFIG_PATH', config['ksys_config_path'])
        if config.get('ub_watch_path'):
            set_key(env_path, 'UB_WATCH_PATH', config['ub_watch_path'])
        if config.get('devkit_cpu_range'):
            set_key(env_path, 'DEVKIT_CPU_RANGE', config['devkit_cpu_range'])
        if config.get('ddr_latency_path'):
            set_key(env_path, 'DDR_LATENCY_PATH', config['ddr_latency_path'])
    else:
        # Fallback: manual write
        with open(env_path, 'a') as f:
            for key, value in config.items():
                if value:
                    env_key = key.upper()
                    if env_key in ENV_REQUIRED_KEYS or env_key == 'DEVKIT_CPU_RANGE':
                        f.write(f"{env_key}={value}\n")


def validate_and_prompt_missing(config: dict) -> dict:
    """Validate paths and prompt user for missing/invalid ones

    Args:
        config: dict with path configurations

    Returns:
        Updated config dict with valid paths or None for disabled tools
    """
    key_mapping = {
        'DEVKIT_PATH': 'devkit_path',
        'KSYS_PATH': 'ksys_path',
        'KSYS_CONFIG_PATH': 'ksys_config_path',
        'UB_WATCH_PATH': 'ub_watch_path',
        'DDR_LATENCY_PATH': 'ddr_latency_path',
    }

    prompt_names = {
        'DEVKIT_PATH': 'DevKit CLI path (devkit executable)',
        'KSYS_PATH': 'ksys executable path',
        'KSYS_CONFIG_PATH': 'ksys config.yaml path',
        'UB_WATCH_PATH': 'ub_watch executable path',
        'DDR_LATENCY_PATH': 'DDR latency tool path (920x_ddr_latency_v1.1.py)',
    }

    for env_key, config_key in key_mapping.items():
        path = config.get(config_key, '')

        # Loop until valid path provided or user skips
        while not path or not os.path.exists(path):
            print(f"\n⚠ {env_key} not configured or path invalid")
            if path:
                print(f"  Current: {path}")
            user_input = input(f"Enter {prompt_names[env_key]} (or 'skip' to disable): ").strip()

            if user_input.lower() == 'skip':
                config[config_key] = None  # Mark as disabled
                print(f"  ✓ {env_key} disabled for this session")
                break

            if os.path.exists(user_input):
                path = user_input
                config[config_key] = user_input
                print(f"  ✓ {env_key} set to: {user_input}")
            else:
                print(f"  ✗ Path does not exist: {user_input}")

    # Save updated config to .env
    save_env_config(config)
    print("\n✓ Configuration saved to .env file")
    return config


def calculate_cpu_range_from_numa(numa_nodes: list) -> str:
    """Calculate CPU core range from NUMA node IDs

    Args:
        numa_nodes: list of NUMA node IDs (e.g., [0, 1])

    Returns:
        CPU range string like "0-95,192-287"
    """
    all_cores = []

    for node in numa_nodes:
        try:
            cpulist_path = f'/sys/devices/system/node/node{node}/cpulist'
            with open(cpulist_path) as f:
                cpulist = f.read().strip()

            # Parse cpulist (e.g., "0-95" or "0,1,2,3-10")
            for part in cpulist.split(','):
                if '-' in part:
                    start, end = part.split('-')
                    all_cores.extend(range(int(start), int(end) + 1))
                else:
                    all_cores.append(int(part))
        except Exception as e:
            print(f"⚠ Failed to read CPU list for NUMA node {node}: {e}")

    if not all_cores:
        return "0-95"  # Fallback default

    # Sort and merge into ranges
    all_cores.sort()
    ranges = []
    start = all_cores[0]
    end = all_cores[0]

    for core in all_cores[1:]:
        if core == end + 1:
            end = core
        else:
            if start == end:
                ranges.append(str(start))
            else:
                ranges.append(f"{start}-{end}")
            start = core
            end = core

    # Add last range
    if start == end:
        ranges.append(str(start))
    else:
        ranges.append(f"{start}-{end}")

    return ','.join(ranges)


# ==================== LogCapture Class ====================

class LogCapture:
    """Parallel log collection with devkit, ksys, ub_watch

    Runs collection tools in background, synchronized with QEMU monitoring duration.
    All output is redirected to log files, not interfering with terminal display.
    """

    # Default timeouts for different tools
    DEFAULT_TOOL_TIMEOUTS = {
        'devkit_mem': 60,      # DevKit usually completes quickly after duration
        'devkit_top_down': 60,
        'ub_watch': 60,
        'ksys': 600,           # ksys needs extra time for data parsing (can be minutes)
        'ddr_latency': 60,     # DDR latency tool
    }

    def __init__(self, config: dict, duration: int, log_dir: str, numa_nodes: list,
                 ksys_parse_timeout: int = None):
        """
        Args:
            config: paths from .env (devkit_path, ksys_path, ksys_config_path, ub_watch_path, devkit_cpu_range)
            duration: collection duration in seconds (same as qemu_monitor -t)
            log_dir: output directory for log files
            numa_nodes: list of NUMA nodes to monitor (for CPU range calculation)
            ksys_parse_timeout: extra timeout for ksys parse phase (default 600s)
        """
        self.config = config
        self.duration = duration
        self.log_dir = log_dir
        self.numa_nodes = numa_nodes
        self.processes = {}  # {tool_name: Popen process}
        self.log_files = {}  # {tool_name: file handle}
        self.failed_startup = []  # tools that failed to start
        self.failed_runtime = []  # tools that failed during runtime
        self.start_time = None
        self.ksys_parse_timeout = ksys_parse_timeout or self.DEFAULT_TOOL_TIMEOUTS['ksys']

    def _get_cpu_range(self) -> str:
        """Get CPU range for devkit top-down command"""
        # Use configured range if available
        if self.config.get('devkit_cpu_range'):
            return self.config['devkit_cpu_range']

        # Calculate from NUMA nodes
        return calculate_cpu_range_from_numa(self.numa_nodes)

    def _start_tool(self, tool_name: str, cmd: list, log_filename: str,
                    success_msg: str = None) -> tuple:
        """Helper to start a single collection tool

        Args:
            tool_name: Name identifier for the tool (e.g., 'devkit_mem')
            cmd: Command list to execute
            log_filename: Log file name (e.g., 'devkit_mem.log')
            success_msg: Optional custom success message

        Returns:
            (success: bool, error_msg: str or None)
        """
        try:
            log_path = os.path.join(self.log_dir, log_filename)
            self.log_files[tool_name] = open(log_path, 'w')
            print(f"  [CMD] {tool_name}: {' '.join(cmd)}")
            self.processes[tool_name] = subprocess.Popen(
                cmd, stdout=self.log_files[tool_name],
                stderr=self.log_files[tool_name],
                cwd=self.log_dir
            )
            if success_msg:
                print(f"  ✓ {success_msg}")
            else:
                print(f"  ✓ Started {tool_name} (duration={self.duration}s)")
            return True, None
        except Exception as e:
            self.failed_startup.append(tool_name)
            print(f"  ✗ Failed to start {tool_name}: {e}")
            return False, str(e)

    def _start_devkit_mem(self) -> tuple:
        """Start DevKit memory tuner"""
        if not self.config.get('devkit_path'):
            return None, None  # Not configured
        cmd = [self.config['devkit_path'], 'tuner', 'memory',
               '-d', str(self.duration), '-i', '2']
        return self._start_tool('devkit_mem', cmd, 'devkit_mem.log',
                                f"Started devkit tuner memory (duration={self.duration}s)")

    def _start_devkit_top_down(self) -> tuple:
        """Start DevKit top-down tuner"""
        if not self.config.get('devkit_path'):
            return None, None  # Not configured
        cpu_range = self._get_cpu_range()
        cmd = [self.config['devkit_path'], 'tuner', 'top-down',
               '-d', str(self.duration), '-i', '2', '-c', cpu_range]
        return self._start_tool('devkit_top_down', cmd, 'devkit_top_down.log',
                                f"Started devkit tuner top-down (cpu_range={cpu_range})")

    def _start_ksys(self) -> tuple:
        """Start ksys collection"""
        if not (self.config.get('ksys_path') and self.config.get('ksys_config_path')):
            return None, None  # Not configured
        cmd = [self.config['ksys_path'], 'collect',
               '-d', str(self.duration), '-i', '2',
               '-c', self.config['ksys_config_path']]
        return self._start_tool('ksys', cmd, 'ksys.log',
                                f"Started ksys collect (config={self.config['ksys_config_path']})")

    def _start_ub_watch(self) -> tuple:
        """Start ub_watch"""
        if not self.config.get('ub_watch_path'):
            return None, None  # Not configured
        cmd = [self.config['ub_watch_path'],
               '-t', str(self.duration), '-i', '2']
        return self._start_tool('ub_watch', cmd, 'ub_watch.log')

    def _start_ddr_latency(self) -> tuple:
        """Start DDR latency tool"""
        if not self.config.get('ddr_latency_path'):
            return None, None  # Not configured
        cmd = ['python3', self.config['ddr_latency_path'],
               '-d', str(self.duration), '-i', '2']
        return self._start_tool('ddr_latency', cmd, 'ddr_latency.log')

    def start(self) -> dict:
        """Start all collection processes in parallel using Popen

        Returns:
            {'success': [tool_names], 'failed': [(tool_name, error_msg)]}
        """
        self.start_time = datetime.now()
        success = []
        failed = []

        # Start each tool
        tools = [
            ('devkit_mem', self._start_devkit_mem),
            ('devkit_top_down', self._start_devkit_top_down),
            ('ksys', self._start_ksys),
            ('ub_watch', self._start_ub_watch),
            ('ddr_latency', self._start_ddr_latency),
        ]

        for tool_name, start_func in tools:
            result, error = start_func()
            if result is None:
                continue  # Tool not configured, skip
            if result:
                success.append(tool_name)
            else:
                failed.append((tool_name, error))

        return {'success': success, 'failed': failed}

    def stop(self):
        """Stop all running processes"""
        for tool_name, proc in self.processes.items():
            if proc.poll() is None:  # Still running
                try:
                    proc.terminate()
                    proc.wait(timeout=5)
                except subprocess.TimeoutExpired:
                    proc.kill()
                except Exception:
                    pass

        # Close log file handles
        for tool_name, f in self.log_files.items():
            try:
                f.close()
            except Exception:
                pass

    def _check_ksys_parse_progress(self) -> str:
        """Check ksys.log for parse progress

        Returns:
            'completed' - parse finished, data output started
            'parsing' - currently parsing data
            'collecting' - still collecting data
            'unknown' - cannot determine status
        """
        ksys_log_path = os.path.join(self.log_dir, 'ksys.log')
        if not os.path.exists(ksys_log_path):
            return 'unknown'

        try:
            with open(ksys_log_path, 'r', encoding='utf-8', errors='ignore') as f:
                content = f.read()

            # Check parse completion markers
            if 'Starting to process and print data' in content:
                return 'completed'
            if 'CPU Metrics' in content or 'Common Microarchitecture Metrics' in content:
                return 'completed'
            if 'Data saved successfully' in content:
                return 'completed'

            # Check if parsing started
            if 'Starting to parse data' in content:
                return 'parsing'

            # Still in collection phase
            if 'Starting to collect data' in content:
                return 'collecting'

            return 'unknown'
        except Exception:
            return 'unknown'

    def _wait_for_ksys(self, proc) -> dict:
        """Wait for ksys with extended timeout for parse phase

        ksys has two phases:
        1. Collect phase: duration seconds (data collection)
        2. Parse phase: can take minutes for large data

        Returns:
            {'status': 'completed'|'timeout', 'returncode': int, 'elapsed': int}
        """
        tool_name = 'ksys'
        start_time = time.time()

        # Phase 1: Wait for collection to complete (duration + buffer)
        collect_timeout = self.duration + 30
        print(f"  [{tool_name}] Waiting for collection phase ({self.duration}s)...")

        try:
            proc.wait(timeout=collect_timeout)
            elapsed = int(time.time() - start_time)
            if proc.returncode == 0:
                print(f"  ✓ {tool_name} completed in {elapsed}s")
                return {'status': 'completed', 'returncode': 0, 'elapsed': elapsed}
            else:
                print(f"  ⚠ {tool_name} exited with code {proc.returncode} after {elapsed}s")
                return {'status': 'completed', 'returncode': proc.returncode, 'elapsed': elapsed}
        except subprocess.TimeoutExpired:
            pass  # Continue to parse phase wait

        # Phase 2: Wait for parse phase with extended timeout
        parse_timeout = self.ksys_parse_timeout
        total_timeout = collect_timeout + parse_timeout
        print(f"  [{tool_name}] Collection done, waiting for parse phase (timeout={parse_timeout}s)...")

        last_status = 'collecting'
        warning_thresholds = [0.5, 0.75, 0.9]
        warnings_given = [False, False, False]

        while time.time() - start_time < total_timeout:
            # Check if process completed
            try:
                proc.wait(timeout=5)
                elapsed = int(time.time() - start_time)
                if proc.returncode == 0:
                    print(f"  ✓ {tool_name} parse completed, total {elapsed}s")
                    return {'status': 'completed', 'returncode': 0, 'elapsed': elapsed}
                else:
                    print(f"  ⚠ {tool_name} exited with code {proc.returncode} after {elapsed}s")
                    return {'status': 'completed', 'returncode': proc.returncode, 'elapsed': elapsed}
            except subprocess.TimeoutExpired:
                pass

            # Check parse progress
            current_status = self._check_ksys_parse_progress()
            elapsed = int(time.time() - start_time)
            elapsed_parse = elapsed - collect_timeout

            if current_status != last_status:
                last_status = current_status
                if current_status == 'parsing':
                    print(f"  [{tool_name}] Parse phase started ({elapsed}s total)")
                elif current_status == 'completed':
                    print(f"  [{tool_name}] Parse completed ({elapsed}s total)")

            # Progress logging every 30s
            if elapsed_parse > 0 and elapsed_parse % 30 == 0:
                progress_ratio = elapsed_parse / parse_timeout
                print(f"  [{tool_name}] Parse in progress... {elapsed_parse}s/{parse_timeout}s ({progress_ratio*100:.0f}%)")

            # Warnings at thresholds
            elapsed_ratio = elapsed_parse / parse_timeout
            for i, threshold in enumerate(warning_thresholds):
                if elapsed_ratio >= threshold and not warnings_given[i]:
                    warnings_given[i] = True
                    remaining = int(parse_timeout - elapsed_parse)
                    print(f"  ⚠ [{tool_name}] Approaching parse timeout ({threshold*100:.0f}% used), {remaining}s remaining")

            time.sleep(5)

        # Timeout - force terminate
        elapsed = int(time.time() - start_time)
        print(f"  ⚠ [{tool_name}] Parse timeout after {elapsed}s total, terminating...")
        proc.terminate()
        time.sleep(3)
        try:
            proc.kill()
        except ProcessLookupError:
            pass

        print(f"  ⚠ {tool_name} timeout suggestions:")
        print(f"      - Increase ksys_parse_timeout (current: {parse_timeout}s)")
        print(f"      - Check ksys.log size and parse progress")
        print(f"      - Reduce monitor sampling interval for less data")

        return {'status': 'timeout', 'returncode': None, 'elapsed': elapsed}

    def wait(self):
        """Wait for all processes to complete, track failures

        Uses different timeouts for different tools:
        - devkit/ub_watch: duration + 60s (quick completion expected)
        - ksys: duration + ksys_parse_timeout (parse can take minutes)
        """
        for tool_name, proc in self.processes.items():
            try:
                # Get appropriate timeout for this tool
                if tool_name == 'ksys':
                    # ksys has special handling with parse phase monitoring
                    result = self._wait_for_ksys(proc)
                    if result['status'] == 'timeout':
                        self.failed_runtime.append({
                            'tool': tool_name,
                            'error': 'parse_timeout',
                            'elapsed': result['elapsed'],
                        })
                    elif result['returncode'] != 0:
                        self.failed_runtime.append({
                            'tool': tool_name,
                            'returncode': result['returncode'],
                        })
                else:
                    # Other tools: simple timeout
                    timeout = self.duration + self.DEFAULT_TOOL_TIMEOUTS.get(tool_name, 60)
                    proc.wait(timeout=timeout)
                    if proc.returncode != 0:
                        self.failed_runtime.append({
                            'tool': tool_name,
                            'returncode': proc.returncode,
                        })
            except subprocess.TimeoutExpired:
                # Process didn't finish within timeout, force terminate
                timeout_used = self.duration + self.DEFAULT_TOOL_TIMEOUTS.get(tool_name, 60)
                print(f"⚠ {tool_name} timed out after {timeout_used}s, terminating...")
                proc.terminate()
                time.sleep(3)
                try:
                    proc.kill()
                except ProcessLookupError:
                    pass
                self.failed_runtime.append({
                    'tool': tool_name,
                    'error': 'timeout',
                })
            except Exception as e:
                self.failed_runtime.append({
                    'tool': tool_name,
                    'error': str(e),
                })

        # Close log file handles
        for tool_name, f in self.log_files.items():
            try:
                f.close()
            except Exception:
                pass

    def get_results(self) -> dict:
        """Return collection results and status

        Returns:
            {
                'success': [tool_names],
                'failed_startup': [tool_names],
                'failed_runtime': [{tool, returncode/error}],
                'log_files': {tool_name: file_path},
                'duration': actual_duration_seconds
            }
        """
        # Calculate actual duration
        actual_duration = 0
        if self.start_time:
            actual_duration = int((datetime.now() - self.start_time).total_seconds())

        # Determine successful tools
        all_tools = list(self.processes.keys())
        success = [t for t in all_tools
                   if t not in self.failed_startup and t not in [f['tool'] for f in self.failed_runtime]]

        return {
            'success': success,
            'failed_startup': self.failed_startup,
            'failed_runtime': self.failed_runtime,
            'log_files': {
                'devkit_mem': os.path.join(self.log_dir, 'devkit_mem.log'),
                'devkit_top_down': os.path.join(self.log_dir, 'devkit_top_down.log'),
                'ksys': os.path.join(self.log_dir, 'ksys.log'),
                'ub_watch': os.path.join(self.log_dir, 'ub_watch.log'),
            },
            'duration': actual_duration,
        }


